import SwiftUI

/// The user's appearance preference, persisted via `@AppStorage("appTheme")`. `.system` tracks
/// the OS appearance (the SwiftUI default when no `.preferredColorScheme` is applied at all).
enum AppTheme: String, CaseIterable, Identifiable {
    case system = "System"
    case light = "Light"
    case dark = "Dark"

    var id: String { rawValue }

    var colorScheme: ColorScheme? {
        switch self {
        case .system: nil
        case .light: .light
        case .dark: .dark
        }
    }

    var icon: String {
        switch self {
        case .system: "circle.lefthalf.filled"
        case .light: "sun.max.fill"
        case .dark: "moon.fill"
        }
    }
}
